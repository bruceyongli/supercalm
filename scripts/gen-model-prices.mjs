#!/usr/bin/env node
// Generate docs/model-prices.json (the Supercalm-hosted pricing manifest, native shape C in
// src/pricing.js) FROM the compiled rules in src/usage_pricing.js — one source of truth, no
// hand-maintained duplicates. Run `npm run gen-prices` after editing RULES; commit the diff.
// The hosted file exists so OTHER installs (and other tools) can one-click a versioned,
// raw.githubusercontent-served price list — and PR corrections against it.
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

process.env.AIOS_DATA = process.env.AIOS_DATA || (await import('node:fs')).mkdtempSync(join((await import('node:os')).tmpdir(), 'aios-genprices-'));
const { priceRuleFor } = await import('../src/usage_pricing.js');
const { listProxyModels } = await import('../src/model_catalog.js');

// Concrete ids come from the live catalog (RULES are regexes); ids without a rule are skipped —
// never guess a price.
const { copyFileSync } = await import('node:fs');
try { copyFileSync(join(process.env.HOME || '', 'aios', 'data', 'model_catalog.json'), join(process.env.AIOS_DATA, 'model_catalog.json')); } catch {}

const models = {};
for (const m of listProxyModels({ includeImages: false })) {
  const r = priceRuleFor({ model: m.id });
  if (!r || r.provider === 'manifest') continue;
  models[m.id] = {
    input: r.input ?? null,
    output: r.output ?? null,
    cached: r.cachedInput ?? r.cacheRead ?? null,
    source: r.source || r.provider,
  };
}

const out = {
  $schema_note: 'Supercalm price manifest (native shape; see src/pricing.js). USD per 1M tokens, api-equivalent estimates for relative cost visibility — subscription CLIs may burn quota, not these dollars. GENERATED by scripts/gen-model-prices.mjs from src/usage_pricing.js RULES — edit RULES + regenerate; PRs welcome. The pricing setting also accepts any LiteLLM model_prices_and_context_window.json URL or an openhand-models.json-style feed.',
  unit: 'per_1m_tokens',
  as_of: new Date().toISOString().slice(0, 10),
  models,
};
const path = join(dirname(fileURLToPath(import.meta.url)), '..', 'docs', 'model-prices.json');
writeFileSync(path, JSON.stringify(out, null, 2) + '\n');
console.log(`wrote ${path}: ${Object.keys(models).length} priced models`);
